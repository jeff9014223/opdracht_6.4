function calculate(a, b) {
    return a + b
}

function multiplyByFive(a) {
    return a * 5
}

function calculateMinutes(sec) {
    return sec / 60
}

console.log(`Antwoord is: ${calculate(5, 5)}`)
console.log(`Antwoord is: ${multiplyByFive(5)}`)
console.log(`Antwoord is: ${calculateMinutes(3600)}`)